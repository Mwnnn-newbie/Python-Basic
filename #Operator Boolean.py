# operator AND == Mengembalikan True jika dua statement sama-sama benar
a = 10
print(a > 7 and a < 11)

# operator OR  == Mengembalikan True jika salah satu statement bernilai benar
a = 10
print(a > 7 or a < 11)

# operator NOT == Menegasikan hasil True menjadi False dan sebaliknya
a = 10
print(not (a > 7 or a < 11))
