# Operator Sama Dengan
# Jika masing-masing operan memiliki nilai yang sama, maka kondisi bernilai benar atau TRUE
a = 10
b = 3
print(a == b)

# Operator tidak Sama Dengan
# Akan mengahasilkan nilai kebalikan dari kondisi sebenarnya
a = 10
b = 3
print(a != b)

# Operator Lebih Besar Dari
# Jika nilai operan kiri lebih besar dari operan kanan, maka kondisi bernilai benar atau TRUE
a = 15
b = 6
print(a > b)

# Operator Lebih Kecil Dari
# Jika nilai operan kiri lebih kecil dari operan kanan, maka kondisi bernilai benar atau TRUE
a = 15
b = 8
print(a < b)

# Operator Lebih Besar samadengan
# Jika nilai operan kiri lebih besar dari operan kanan, atau sama maka kondisi bernilai benar atau TRUE
a = 15
b = 7
print(a >= b)

# Operator Lebih Kecil samadengan
# Jika nilai operan kiri lebih kecil dari operan kanan, atau sama maka kondisi bernilai benar atau TRUE
a = 15
b = 7
print(a <= b)
