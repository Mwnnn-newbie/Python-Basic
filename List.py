# List yang berisi kumpulan string
list_buah = ['Pisang','Nanas','Semangka','Salak']

print('list_buah = ',list_buah)

print('list_buah[1] = ',list_buah[:1])
print('list_buah[2] = ',list_buah[0:2])
print('list_buah[3] = ',list_buah[0:3])
print('list_buah[4] = ',list_buah[:-1])
print('list_buah[5] = ',list_buah[2:-3])
print('list_buah[6] = ',list_buah[:-3])
print('list_buah[7] = ',list_buah[-3:-1])

